function paginationinit() {
      // Items per page
    const itemsPerPage = 30;
        // Current page
    let currentPage = 1;
    // Get all movie items
    const movieItem = moviesContainer.querySelectorAll('.inner-div');
    let movieItems = Array.from(movieItem);
  
    // Show initial items for the first page
    showItems(movieItems, currentPage, itemsPerPage);

    // Generate initial page numbers
    generatePageNumbers(movieItems, currentPage, itemsPerPage);
}

// Function to show items for the current page
function showItems(movieItems, currentPage, itemsPerPage) {
    const startIndex = (currentPage - 1) * itemsPerPage;
    const endIndex = startIndex + itemsPerPage;
    
    movieItems.forEach((item, index) => {
        if (index >= startIndex && index < endIndex) {
            item.style.display = 'block';
        } else {
            item.style.display = 'none';
        }
    });
}

// Function to go to a specific page
function goToPage(page, movieItems, currentPage, itemsPerPage) {
    currentPage = page;
    showItems(movieItems, currentPage, itemsPerPage);
    generatePageNumbers(movieItems, currentPage, itemsPerPage);
}

// Generate page numbers
function generatePageNumbers(movieItems, currentPage, itemsPerPage) {
    const totalPages = Math.ceil(movieItems.length / itemsPerPage);
    const pageNumbersDiv = document.getElementById('pageNumbers');
    
    let pageNumbersHTML = '';
    let startPage = Math.max(currentPage - 2, 1);
    let endPage = Math.min(startPage + 4, totalPages);

    if (startPage > 1) {
        pageNumbersHTML += '<<';
    }
    
    for (let i = startPage; i <= endPage; i++) {
        pageNumbersHTML += `<span class="pageNumber">${i}</span>`;
    }
    
    if (endPage < totalPages) {
        pageNumbersHTML += ' >>';
    }
    
    pageNumbersDiv.innerHTML = pageNumbersHTML;

    // Attach click event listeners to page numbers
    const pageNumbers = Array.from(document.querySelectorAll('.pageNumber'));
    pageNumbers.forEach((pageNumber) => {
        pageNumber.addEventListener('click', () => {
            const page = Number(pageNumber.textContent);
            const currentState = history.state;
            if (!currentState || currentState.pageId !== page) {
                history.pushState({ pageId: 'page' }, '', '#' + page);
            }
            goToPage(page, movieItems, currentPage, itemsPerPage);
        });
    });
}

