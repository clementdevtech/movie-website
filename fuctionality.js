// Function to generate a unique reference ID
function generateReference(phoneNumber) {
    // Generate random string
    const randomString = Math.random().toString(36).substr(2, 5); // Random string of length 5

    // Generate timestamp (format: YYMMDDHHmmss)
    const now = new Date();
    const timestamp = `${now.getFullYear().toString().substr(-2)}${(now.getMonth() + 1).toString().padStart(2, '0')}${now.getDate().toString().padStart(2, '0')}${now.getHours().toString().padStart(2, '0')}${now.getMinutes().toString().padStart(2, '0')}${now.getSeconds().toString().padStart(2, '0')}`;

    // Concatenate phone number, random string, and timestamp
    const concatenatedID = `${phoneNumber}${randomString}${timestamp}`;

    // Trim to length 8
    const generatedID = concatenatedID.substring(0, 8);

    return generatedID;
}
